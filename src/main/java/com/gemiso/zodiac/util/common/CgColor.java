package com.gemiso.zodiac.util.common;

public class CgColor {

	public int Index;
	public int Count;
	public String RGBString;

	public int getIndex() {
		return Index;
	}

	public void setIndex(int index) {
		Index = index;
	}

	public int getCount() {
		return Count;
	}

	public void setCount(int count) {
		Count = count;
	}

	public String getRGBString() {
		return RGBString;
	}

	public void setRGBString(String rGBString) {
		RGBString = rGBString;
	}

}
